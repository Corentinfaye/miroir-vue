                     nombre des florilèges classiques :</p>
        <ul>
            <li>Birger Munk Olsen, « Les classiques latins dans les florilèges médiévaux antérieurs au XIIIe siècle », dans <em>Revue d'histoire des textes</em>, t. 9, 1979, p. 47-121 et t. 10,
                        1980, p. 115-164. </li>
        </ul>
