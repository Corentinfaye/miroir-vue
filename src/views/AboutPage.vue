                        traductions<a onmouseout="HideBulle()" onmouseover="AffBulle('L'Egloga Theoduli a été traduite en français par Jean Le Fèvre. Voir Geneviève Hasenohr, Tradition du texte et tradition de l'image. A propos d'un programme d'illustration du Theodelet, dans Miscellanea codicologica F. Masai dicat 1979, ediderunt Pierre Cockshaw, Monique-Cécile Garand et Pierre Jodogne, 1979, p. 451-467.')" id="appelnote6" href="#footnote6" class="appel_note">6</a> ; ils sont en tout cas situés au même niveau d'autorité que les textes de
