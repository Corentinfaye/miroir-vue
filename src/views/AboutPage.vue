        </ul>
        <h3>Borne chronologique</h3>
        <p class="texte">Utiliser la notion  de « classique » oblige 
néanmoins à établir une limite chronologique. 529 est la date de
                     fermeture de l'Académie de Platon par Justinien et 
celle de la fondation de la communauté du Mont-Cassin par
                     saint Benoît. Boèce, qui avait l'intention de 
traduire les œuvres complètes de Platon et d'Aristote pour les
                     rendre accessibles à l'Occident latin, est mort en 
524. Ces dates (524-529) marquent symboliquement la fin
                     d'une tradition et le début d'une autre. Émerge 
alors, avec la conscience du passage à des temps nouveaux, la
                     nécessité d'une synthèse des  connaissances : 
l’élaboration du <em>Corpus juris civilis</em> (528-534)
                     peut être interprétée comme le parallèle juridique 
du projet non abouti de synthèse philosophique par Boèce.
                     Sont donc définis dans le <em>Miroir des classiques</em> comme « classiques » les auteurs et les textes
                     païens antérieurs à la seconde moitié du VI<sup>e</sup> siècle : nous avons ainsi exclu, comme Jacques Monfrin, la
                        <em>Cité de Dieu</em> même si, dans la traduction qu'il en a faite pour Charles V, Raoul de Presles
                     a commenté les dix premiers livres comme s'il s'était agi d'un texte historique, en en gommant les aspects
                     polémiques et apologétiques. En revanche, nous avons inclus, contrairement à Jacques Monfrin, la
                        <em>Consolation de Philosophie</em> et le <em>Corpus juris civilis</em> de Justinien. Si en effet Boèce est rangé parmi les auteurs patristiques dans
                     nombre d'histoires de la littérature latine, la qualité de penseur chrétien lui fut parfois contestée au
                     Moyen Âge, plusieurs commentateurs carolingiens ayant jugé la <em>Consolation</em> dangereuse pour la
                     foi, car son texte reflète plus la philosophie et la mythologie antique que la foi chrétienne<a onmouseout="HideBulle()" onmouseover="AffBulle('Sur Boèce, voir un point récent dans Boèce ou la chaine des savoirs. Actes du colloque international de la fondation Singer-Polignac (Paris, 8-12 juin 1999) édités pr Alain Galonnier, Louvain, 2003, particulièrement Umberto Todini, Boezio più pagano di Lucrezio?, p. 23-31.')" id="appelnote4" href="#footnote4" class="appel_note">4</a>. Quant au <em>Corpus juris civilis</em>, s’il est compilé dans un empire chrétien, il repose largement sur le droit classique.</p>
        <h3>Apocryphes</h3>
        <p class="texte">Notre point de vue étant celui du Moyen Âge, nous avons inclus systématiquement, contrairement à Jacques
                     Monfrin, les textes apocryphes, même s'ils sont quelquefois postérieurs au VI<sup>e</sup> siècle et explicitement
                     chrétiens. Le <em>De vetula</em> que la critique moderne attribue à Richard de Fournival (XIII<sup>e</sup> siècle)
                     est un poème en trois chants dans lequel Ovide est censé raconter un épisode de sa vie amoureuse: il a été
                     attribué par le Moyen Âge à Ovide et comme tel il a été traduit en français par Jean Le Fèvre en 1376. De
                     même le traité de morale du moine Martin de Braga (510-519) intitulé <em>Formula honestae vitae</em>
                     est attribué par le Moyen Âge à Sénèque et a été traduit trois fois du XIII<sup>e</sup> au XV<sup>e</sup> siècle. Jean-Yves
                     Tilliette a consacré à ces textes une rapide mais judicieuse synthèse dans le <em>Dictionnaire du Moyen
                        Âge</em><a onmouseout="HideBulle()" onmouseover="AffBulle('Jean-Yves Tilliette Imitation des classiques, dans Dictionnaire du Moyen Âge sous la direction de Claude Gauvard, Alain de Libera et Michel Zink, Paris, 2002, p. 298-300.')" id="appelnote5" href="#footnote5" class="appel_note">5</a> et l'anthologie de Paul Lehmann en donne un
                     échantillonnage centré sur Ovide :</p>
        <ul>
            <li>Paul Lehmann, <em>Pseudo-antike Literatur des Mittelalters</em>, Leipzig, 1927. </li>
